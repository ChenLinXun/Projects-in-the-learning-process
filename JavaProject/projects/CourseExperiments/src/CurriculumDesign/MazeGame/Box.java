package CurriculumDesign.MazeGame;

public class Box {

    private int boxX;//箱子横坐标
    private int boxY;//箱子纵坐标

    public int getBoxX() {
        return boxX;
    }

    public void setBoxX(int boxX) {
        this.boxX = boxX;
    }

    public int getBoxY() { return boxY; }

    public void setBoxY(int boxY) {
        this.boxY = boxY;
    }
}
